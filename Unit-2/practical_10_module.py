# Function for Dearness Allowance (DA) - assume 10% of basic
def DA(basic):
    return basic * 0.10

# Function for House Rent Allowance (HRA) - assume 15% of basic
def HRA(basic):
    return basic * 0.15

# Function for Provident Fund (PF) - assume 12% of basic
def PF(basic):
    return basic * 0.12

# Function for Income Tax (ITAX) - assume 10% of gross salary
def ITAX(gross):
    return gross * 0.10
